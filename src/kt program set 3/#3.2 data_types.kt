// defining a value

fun main(args: Array<String>){

    var age: Int = 10
    //or you can simply write
    // var age  = 10

    var b: Boolean = true
    var f: Float = 97.4F
    //F is necessary to denote for Float Data Type
    var d: Double = 98.47

    println(age)
    println(b)
    println(f)
    print(d)
}

