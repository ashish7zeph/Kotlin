fun main(args: Array<String>){

    var myString = "Hello World!" //var is to declare a variable
    var num = 99
    var decimal = 99.5
    var myChar = 'a'
    var bool = false

    val myAnotherStr = "My constant string value"
    //val is to declare a constant variable
    //if its value is tried to change, will result in an error

    println(myString)
    println(num)
    println(decimal)
    println(myChar)
    println(bool)
    print(myAnotherStr)

}